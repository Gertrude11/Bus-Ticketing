     ///method for adding rows in table
    public void addRows(){  
        try{
            Registry registry = LocateRegistry.getRegistry("127.0.0.1",81);
            BusIntrf service  = (BusIntrf)registry.lookup("Bus");
            List<BusModel> busList = service.allBus();
          
          
            if(busList!=null){
                  Iterator iterator = busList.iterator();
                 while (iterator.hasNext()){
                BusModel cars = (BusModel)iterator.next();
                table.addRow(new Object[]{
                   cars.getBusId(),
                   cars.getModel(),
                   cars.getRoute(),
                   cars.getPrice(),
                   cars.getDeparture_date(),
                   cars.getDeparture_time(),
                   cars.getArrival_time(),
                   cars.getSeats()
               
                });
                bus_table.setModel(table);  
            }
           
            
            }
          
            
        }catch(Exception exe){
            exe.printStackTrace();
        }
    }

