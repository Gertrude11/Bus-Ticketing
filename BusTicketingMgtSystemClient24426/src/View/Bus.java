    private void searchBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtn1ActionPerformed
        // TODO add your handling code here:
        try{
            
            BusModel car = new BusModel();
            car.setBusId(searchTxt.getText());
             
            Registry registry = LocateRegistry.getRegistry("127.0.0.1",81);
            BusIntrf service = (BusIntrf)registry.lookup("Bus");
            BusModel buses = service.searchId(car);
            //set table to 0 rows
            table.setRowCount(0);
            if(buses != null){
               table.addRow(new Object[]{
                    buses.getBusId(),
                  
                   buses.getModel(),
                   buses.getRoute(),
                   buses.getPrice(),
                   buses.getDeparture_date(),
                   buses.getDeparture_time(),
                   buses.getArrival_time(),
                   buses.getSeats()
                    
                });
            }
        }catch(Exception exe){
            exe.printStackTrace();
        }
        
    }//GEN-LAST:event_searchBtn1ActionPerformed

