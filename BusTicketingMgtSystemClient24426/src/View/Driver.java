     private void addItemCombo(){
        try{
            busCombo.removeAllItems();
            Registry registry = LocateRegistry.getRegistry("127.0.0.1",81);
            BusIntrf service = (BusIntrf)registry.lookup("Bus");
            List<BusModel> buses = service.allBus();
            if(buses != null){
                for(BusModel bus: buses){
                    busCombo.addItem(bus.getBusId());
                }
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
