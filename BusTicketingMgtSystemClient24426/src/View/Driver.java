    public void addRows(){  
        try{
            
            Registry registry = LocateRegistry.getRegistry(81);
            DriverIntrf service  = (DriverIntrf)registry.lookup("Driver");
            List<DriverModel> driverList = service.allDrivers();
            //List<DriverModel> carList = service.selectedBus();
            
            
            Iterator iterator = driverList.iterator();
            
            
            
           // String busData = DriverIntrf.fetchBusData(busId);
          
            while (iterator.hasNext()){
                DriverModel drove = (DriverModel)iterator.next();
                    //String foreignKeyData = drove.getBus().getBusId();// Fetch the related data for the foreign key

                String foreignKeyData = null;
                if (drove != null && drove.getBus() != null) {
                    foreignKeyData = drove.getBus().getBusId();
                }
                table.addRow(new Object[]{
                   drove.getDriverId(),
                    drove.getFirstName(),
                    drove.getLastName(),
                    drove.getDOB(),
                    drove.getPhone_number(),
                    //drove.getBus().getBusId()
                    foreignKeyData
                  
                });
            
            }
          driver_table.setModel(table);  
            
        }catch(Exception exe){
            exe.printStackTrace();
        }
    }

